
public class Limit {
	
	public static int limitLong(int reference){
		
		int verificador = 0;
		
		if((reference == 19)||(reference == 18)){
			
			verificador  = 1;
		}
		
		return verificador;
	}
	
	public static int limitSmall(int secondReference){
		
		int verificador = 0;
		
		if(secondReference == 19){
			
			verificador = 1;
		}
		
		return verificador;
	}

}
